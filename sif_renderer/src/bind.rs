use gl::types::*;
// ////////////////////////////////////////////////////////////////////////////
// ============================================================================
/// trait Bind
pub trait Bind {
    // ========================================================================
    /// id
    fn id(&self) -> GLuint;
    // ========================================================================
    /// bind
    fn bind(&self);
    // ========================================================================
    /// unbind
    fn unbind(&self);
    // ========================================================================
    /// bind_with
    fn bind_with<R>(&self, func: impl FnOnce() -> R) -> R {
        self.bind();
        let ret = func();
        self.unbind();
        ret
    }
}
