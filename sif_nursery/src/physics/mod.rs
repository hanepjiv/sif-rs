/// const GRAVITY_F32
pub const GRAVITY_F32: f32 = 9.80665f32;
// ----------------------------------------------------------------------------
/// const GRAVITY_F64
pub const GRAVITY_F64: f64 = 9.80665f64;
// ////////////////////////////////////////////////////////////////////////////
// ============================================================================
/// trait Gravity
pub trait Gravity<V> {
    // ========================================================================
    /// gravity
    fn gravity() -> V;
}
// ============================================================================
impl Gravity<f32> for f32 {
    fn gravity() -> f32 {
        GRAVITY_F32
    }
}
impl Gravity<f64> for f64 {
    fn gravity() -> f64 {
        GRAVITY_F64
    }
}
